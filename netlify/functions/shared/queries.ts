export const TRENDS_SQL = `
  SELECT
    DATE_TRUNC('month', LISTING_PICK_UP_DATE) as period,
    SUM(CASE
      WHEN LISTING_COMPLETION_STATUS = 'Completed Paid'
      THEN 1 ELSE 0
    END) as jobs,
    SUM(LISTING_TTV) as ttv,
    SUM(AV_FEE) as av_fee,
    ROUND(SUM(AV_FEE) / NULLIF(SUM(LISTING_TTV), 0) * 100, 2) as margin_pct,
    SUM(ALLOCATION_SPEND_TP_AMOUNT_CHANGE) as alloc_spend,
    ROUND(SUM(ALLOCATION_SPEND_TP_AMOUNT_CHANGE) / NULLIF(SUM(LISTING_TTV), 0) * 100, 2) as spend_ttv_pct,
    SUM(CASE
      WHEN DAYS_FROM_CANCELLATION_TO_PICK_UP IN (0, -1)
      THEN 1 ELSE 0
    END) as otd_cancels,
    SUM(CASE
      WHEN LISTING_ALLOCATION_STATUS = 'Third Party Cancelled'
      THEN 1 ELSE 0
    END) as tp_cancels,
    SUM(CASE
      WHEN CANCELLATION_REASON = 'av_couldnt_source_a_tp'
      THEN 1 ELSE 0
    END) as cant_source_count,
    SUM(OTD_DEALLOCATION_COUNT) as otd_deallocations,
    COUNT(*) as total_records
  FROM MART_ENTERPRISE.PRODUCTION.ENTERPRISE_LISTING_EXTRACT
  WHERE ${BASE_FILTER}
    AND LISTING_PICK_UP_DATE >= DATEADD('month', -13, DATE_TRUNC('month', CAST(? AS DATE)))
    AND LISTING_PICK_UP_DATE <= DATE_TRUNC('month', CAST(? AS DATE))
  GROUP BY DATE_TRUNC('month', LISTING_PICK_UP_DATE)
  ORDER BY period ASC
`;
